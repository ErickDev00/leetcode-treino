public class P125_ValidPalindrome {
    public boolean isPalindrome(String s) {

        if (s.length() == 1) {
            return true;
        }

        s = s.toLowerCase();
        int l=0;
        int r = s.length() - 1;

        while(l < r){

            while (l < r && !Character.isLetterOrDigit(s.charAt(l)) ) {
                l++;
            }

            while (l < r && !Character.isLetterOrDigit(s.charAt(r)) ) {
                r--;
            }

            if (s.charAt(l) != s.charAt(r)) {
                return false;
            }

            l++;
            r--;

        }
        return true;
    }
}
